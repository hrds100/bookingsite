/**
 * Post-build script: embeds the built dist/index.html into api/_template.ts
 * so the Vercel Edge Function can inject OG tags without any runtime HTTP fetch.
 *
 * Run automatically as part of `npm run build`.
 */

const fs   = require('fs');
const path = require('path');

const htmlPath    = path.join(__dirname, '../dist/index.html');
const outputPath  = path.join(__dirname, '../api/_template.ts');

if (!fs.existsSync(htmlPath)) {
  console.error('[generate-api-template] dist/index.html not found — skipping');
  process.exit(0);
}

const html = fs.readFileSync(htmlPath, 'utf8');

const output = `// ⚠️  AUTO-GENERATED by scripts/generate-api-template.cjs
// Do not edit manually — re-run \`npm run build\` to regenerate.
export const HTML_TEMPLATE = ${JSON.stringify(html)};
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, output, 'utf8');
console.log('[generate-api-template] wrote api/_template.ts ✓');

// Remove dist/index.html so Vercel's static-file layer doesn't serve it
// directly, bypassing the Edge Function rewrite. All HTML routes (including /)
// will fall through to /(.*) → /api/render where OG tags are injected.
fs.unlinkSync(htmlPath);
console.log('[generate-api-template] removed dist/index.html (edge function owns all HTML) ✓');
